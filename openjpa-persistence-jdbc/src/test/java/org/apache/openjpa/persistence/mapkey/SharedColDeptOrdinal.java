package org.apache.openjpa.persistence.mapkey;

import java.util.HashMap;
import java.util.Map;

import jakarta.persistence.Access;
import jakarta.persistence.AccessType;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.MapKeyColumn;
import jakarta.persistence.MapKeyEnumerated;
import jakarta.persistence.OneToMany;
import jakarta.persistence.Table;

@Entity
@Table(name = "SC_DEPT")
@Access(AccessType.PROPERTY)
public class SharedColDeptOrdinal {

    private long id;
    private String name;
    private Map<SharedColOffices, SharedColEmpOrdinal> employees =
        new HashMap<>();

    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @OneToMany(mappedBy = "department")
    @MapKeyColumn(name = "OFFICE_ID")
    @MapKeyEnumerated()
    public Map<SharedColOffices, SharedColEmpOrdinal> getEmployees() {
        return employees;
    }

    public void setEmployees(
            Map<SharedColOffices, SharedColEmpOrdinal> employees) {
        this.employees = employees;
    }
}
